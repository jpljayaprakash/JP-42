#include <unistd.h>

int main(int ac, char **av)
{
	int i;
	int count[128];
	if (ac == 3)
	{
	i = -1;
	while(++i<128)
		count[i]=0;
	i = -1;
	while(av[1][++i])
	{
	if(count[(int)av[1][i]] == 0)
	{
	write(1,&av[1][i],1);
	count[(int)av[1][i]] = 1;
	}
	}
	}
	write(1,"\n",1);
	return(0);
}
