#include <unistd.h>

int main(int ac, char **av)
{
	int i = -1;
	
	if (ac == 2)
	{
	while (av[1][++i] != '\0')
	{
	if(av[1][i] >= 'a' && av[1][i] <='z')
		av[1][i] = 'm' - (av[1][i] - 'n');
        else if(av[1][i] >= 'A' && av[1][i] <='Z')
                av[1][i] = 'M' - (av[1][i] - 'N');
	}
	i = -1;
	while (av[1][++i] != '\0')
		write(1, &av[1][i],1);
	}
	write(1,"\n",1);
}

	
