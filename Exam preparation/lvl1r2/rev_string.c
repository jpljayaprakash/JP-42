#include <unistd.h>

int main (int ac, char **av)
{
	int i = 0;
	int j = -1;
	char t;

	if (ac == 2)
	{
		while (av[1][i] != '\0')
			i++;
		while(++j < --i)
		{
			t = av[1][i];
			av[1][i] = av[1][j];
			av[1][j] = t;
		}
        j = -1;
        while(av[1][++j] != '\0')
                write(1, &av[1][j],1);	
	}

	write(1, "\n",1);
}
						
		
