#include <unistd.h>

int	ft_strlen(char *str)
{
	int i = -1;
	while (str[++i] != '\0')
		i++;
	return i;
}

int main()
{
 char s[] = "jaya";

printf("%d",ft_strlen(s));
return 0;
}

